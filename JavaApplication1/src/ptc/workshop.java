    @Override
    public void actionPerformed(ActionEvent e) {

    if(e.getActionCommand()=="stock management")
        {
        remove(middle);
        remove(middle2);
        add(middle1);
         validate();
         repaint();
        }
    if(e.getActionCommand()=="expense")
        {
        remove(middle1);
        remove(middle2);
        add(middle);
        validate();
        repaint();
        }
    if(e.getActionCommand()=="service")
        {
        
        remove(middle1);
        remove(middle);
        add(middle2);
        validate();
        repaint();
        }
    //System.out.print(e.getActionCommand());
     if(e.getActionCommand()=="logout")
        {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "ARE YOU SURE??", "LOGOUT",dialogButton);
            if(dialogResult==0)
            {
                //System.out.println("Yes option");
                try {
                    new Login().setVisible(true);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(workshop.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(workshop.class.getName()).log(Level.SEVERE, null, ex);
                }
                    
                dispose();

            }
        
        }
     if(e.getSource()==modulebox)
     {
          String module_name=modulebox.getSelectedItem().toString();
          int selectedIndex = modulebox.getSelectedIndex();
         // System.out.print(selectedIndex);
        if(module_name=="office")
        {
           int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "ARE YOU SURE??", "change module?",dialogButton);
            if(dialogResult==0)
            {
                //System.out.println("Yes option");
                new office().setVisible(true);
                dispose();
            }
            else
            {            
               modulebox.setSelectedIndex(0);
            }
        }
     }
     
   }//action perform method end here

   

